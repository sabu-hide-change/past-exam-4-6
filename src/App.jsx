/* ===================== メインアプリ ===================== */
export default function App() {
  const [screen, setScreen] = useState("login"); // login | dashboard | quiz | result
  const screenRef = useRef(screen);
  useEffect(() => {
    screenRef.current = screen;
  }, [screen]);

  const [userId, setUserId] = useState("");
  const [inputId, setInputId] = useState("");
  const [loading, setLoading] = useState(false);

  const [history, setHistory] = useState({});
  const [reviews, setReviews] = useState({});
  const [progress, setProgress] = useState({ progressIndex: 0, progressMode: "all" });

  const [mode, setMode] = useState("all");
  const [quizList, setQuizList] = useState([]);
  const [currentIndex, setCurrentIndex] = useState(0);
  const [selected, setSelected] = useState(null);
  const [isAnswered, setIsAnswered] = useState(false);

  const [showResumeModal, setShowResumeModal] = useState(false);
  const [pendingProgress, setPendingProgress] = useState(null);

  const isFirstLoad = useRef(true);
  useEffect(() => {
    isFirstLoad.current = true;
  }, [userId]);

  // データ適用ヘルパ
  const applyData = (data) => {
    setHistory(data.history || {});
    setReviews(data.reviews || {});
    setProgress({
      progressIndex: Number(data.progressIndex || 0),
      progressMode: data.progressMode || "all",
    });
  };

  /* 合言葉ログイン後：認証→Firestore購読（screenRefガード付き） */
  useEffect(() => {
    if (!userId) return;
    let unsub = () => {};
    let cancelled = false;

    const setup = async () => {
      setLoading(true);
      // まず LocalStorage から即時復元（オフライン耐性）
      const local = loadLocal(userId);
      if (local) {
        applyData(local);
        console.log("[load] LocalStorage から復元");
      }
      try {
        if (firebaseReady) {
          await signInAnonymously(auth);
          console.log("[auth] 匿名サインイン成功");
          const ref = doc(db, APP_ID, userId);
          unsub = onSnapshot(
            ref,
            (snapshot) => {
              if (cancelled) return;
              const data = snapshot.exists() ? snapshot.data() : {};
              const parsed = {
                progressIndex: Number(data.progressIndex || 0),
                progressMode: data.progressMode || "all",
                history: data.history || {},
                reviews: data.reviews || {},
              };
              // 履歴・要復習は常に同期
              setHistory(parsed.history);
              setReviews(parsed.reviews);
              setProgress({
                progressIndex: parsed.progressIndex,
                progressMode: parsed.progressMode,
              });
              saveLocal(userId, parsed);

              // 【ガード】初回ロードかつダッシュボード表示中のみ途中再開モーダルを起動
              if (isFirstLoad.current && screenRef.current === "dashboard") {
                isFirstLoad.current = false;
                if (parsed.progressIndex > 0) {
                  console.log("[resume] 途中再開モーダルを表示", parsed.progressIndex);
                  setPendingProgress(parsed);
                  setShowResumeModal(true);
                }
              }
              setLoading(false);
            },
            (err) => {
              console.warn("[snapshot] エラー。LocalStorage継続", err);
              setLoading(false);
            }
          );
        } else {
          // Firebase未設定でもローカルのみで再開判定
          if (isFirstLoad.current && screenRef.current === "dashboard") {
            isFirstLoad.current = false;
            const p = local || { progressIndex: 0, progressMode: "all" };
            if (Number(p.progressIndex || 0) > 0) {
              setPendingProgress({
                progressIndex: Number(p.progressIndex || 0),
                progressMode: p.progressMode || "all",
              });
              setShowResumeModal(true);
            }
          }
          setLoading(false);
        }
      } catch (e) {
        console.warn("[setup] 認証/Firestore失敗。LocalStorageのみで継続", e);
        setLoading(false);
      }
    };

    setup();
    return () => {
      cancelled = true;
      unsub();
    };
  }, [userId]);

  /* Firestore + LocalStorage への保存 */
  const persist = async (fields) => {
    if (!userId) return;
    const merged = {
      history,
      reviews,
      progressIndex: progress.progressIndex,
      progressMode: progress.progressMode,
      ...fields,
    };
    saveLocal(userId, merged);
    if (firebaseReady && auth?.currentUser) {
      try {
        await setDoc(doc(db, APP_ID, userId), merged, { merge: true });
      } catch (e) {
        console.warn("[save] Firestore保存失敗（ローカルは保存済）", e);
      }
    }
  };

  /* ログイン */
  const handleLogin = () => {
    const id = inputId.trim();
    if (!id) return;
    console.log("[login] userId =", id);
    setUserId(id);
    setScreen("dashboard");
  };

  /* 出題リスト構築 */
  const buildList = (m) => {
    if (m === "wrong")
      return QUESTIONS.filter((q) => history[q.id] && history[q.id].correct === false);
    if (m === "review") return QUESTIONS.filter((q) => reviews[q.id]);
    return QUESTIONS;
  };

  const startMode = (m, startIndex = 0) => {
    const list = buildList(m);
    if (!list || list.length === 0) {
      alert("対象となる問題がありません。");
      return;
    }
    const idx = Math.min(Math.max(0, startIndex), list.length - 1);
    setMode(m);
    setQuizList(list);
    setCurrentIndex(idx);
    setSelected(null);
    setIsAnswered(false);
    setProgress({ progressIndex: idx, progressMode: m });
    setScreen("quiz");
    console.log("[startMode]", m, "from", idx, "len", list.length);
    persist({ progressIndex: idx, progressMode: m });
  };

  /* 解答 */
  const handleAnswer = (label) => {
    if (isAnswered) return;
    const q = quizList[currentIndex];
    const correct = label === q.answer;
    const newHistory = {
      ...history,
      [q.id]: { correct, choice: label, ts: Date.now() },
    };
    const nextIndex = currentIndex + 1;
    setSelected(label);
    setIsAnswered(true);
    setHistory(newHistory);
    setProgress({ progressIndex: nextIndex, progressMode: mode });
    console.log("[answer] q", q.id, correct ? "○" : "×", "progressIndex→", nextIndex, "mode", mode);
    persist({ history: newHistory, progressIndex: nextIndex, progressMode: mode });
  };

  /* 次の問題へ */
  const nextQuestion = () => {
    const ni = currentIndex + 1;
    if (ni >= quizList.length) {
      setProgress({ progressIndex: 0, progressMode: mode });
      persist({ progressIndex: 0, progressMode: mode });
      console.log("[complete] 全問完走。progressIndex を 0 にリセット");
      setScreen("result");
    } else {
      setCurrentIndex(ni);
      setSelected(null);
      setIsAnswered(false);
      console.log("[next] index →", ni);
    }
  };

  /* ホームに戻る（現在地を保存） */
  const goHome = () => {
    persist({ progressIndex: progress.progressIndex, progressMode: mode });
    console.log("[home] progressIndex を保存", progress.progressIndex);
    setShowResumeModal(false);
    isFirstLoad.current = false;
    setScreen("dashboard");
    setSelected(null);
    setIsAnswered(false);
  };

  /* 要復習トグル */
  const toggleReview = (qid) => {
    const newReviews = { ...reviews, [qid]: !reviews[qid] };
    setReviews(newReviews);
    persist({ reviews: newReviews });
    console.log("[review] q", qid, "→", !!newReviews[qid]);
  };

  /* 再開 / 最初から */
  const handleResume = () => {
    const p = pendingProgress || progress;
    setShowResumeModal(false);
    const list = buildList(p.progressMode);
    if (!list || list.length === 0) {
      startMode("all", 0);
      return;
    }
    const idx = Math.min(Math.max(0, p.progressIndex), list.length - 1);
    setMode(p.progressMode);
    setQuizList(list);
    setCurrentIndex(idx);
    setSelected(null);
    setIsAnswered(false);
    setScreen("quiz");
    console.log("[resume] mode", p.progressMode, "index", idx);
  };

  const handleRestart = () => {
    setShowResumeModal(false);
    setProgress({ progressIndex: 0, progressMode: "all" });
    persist({ progressIndex: 0, progressMode: "all" });
    console.log("[restart] progressIndex を 0 にリセット");
  };

  /* ===== 集計 ===== */
  const stats = useMemo(() => {
    let correct = 0,
      wrong = 0,
      untouched = 0;
    QUESTIONS.forEach((q) => {
      const h = history?.[q.id];
      if (!h) untouched += 1;
      else if (h.correct) correct += 1;
      else wrong += 1;
    });
    const reviewCount = QUESTIONS.filter((q) => reviews?.[q.id]).length;
    return { correct, wrong, untouched, reviewCount, total: QUESTIONS.length };
  }, [history, reviews]);

  const chartData = [
    { name: "正解", value: stats.correct, fill: "#22c55e" },
    { name: "不正解", value: stats.wrong, fill: "#ef4444" },
    { name: "未着手", value: stats.untouched, fill: "#475569" },
  ];

  /* ===================== 画面：ログイン ===================== */
  if (screen === "login" || !userId) {
    return (
      <div className="min-h-screen bg-slate-950 text-slate-100 flex items-center justify-center p-6 font-sans">
        <div className="w-full max-w-md rounded-2xl border border-slate-800 bg-slate-900/70 p-8 shadow-2xl shadow-indigo-950/40">
          <div className="mb-6 text-center">
            <div className="mx-auto mb-4 flex h-14 w-14 items-center justify-center rounded-2xl bg-gradient-to-br from-indigo-500 to-sky-500 shadow-lg shadow-indigo-900/50">
              <BookOpen size={28} className="text-white" />
            </div>
            <h1 className="text-xl font-bold tracking-tight">過去問セレクト演習</h1>
            <p className="mt-1 text-sm text-slate-400">4-6 情報システムの開発</p>
          </div>
          <label className="mb-2 block text-sm text-slate-300">合言葉（ユーザーID）</label>
          <div className="flex items-center gap-2 rounded-xl border border-slate-700 bg-slate-950 px-3 py-2 focus-within:border-indigo-500 transition-colors">
            <User size={18} className="text-slate-500" />
            <input
              value={inputId}
              onChange={(e) => setInputId(e.target.value)}
              onKeyDown={(e) => e.key === "Enter" && handleLogin()}
              placeholder="例：taro-2026"
              className="w-full bg-transparent text-slate-100 placeholder-slate-600 outline-none"
            />
          </div>
          <p className="mt-2 text-xs text-slate-500">
            同じ合言葉をPC・スマホで入力すると、学習履歴と進捗が同期・復元されます。
          </p>
          <button
            onClick={handleLogin}
            disabled={!inputId.trim()}
            className="mt-6 flex w-full items-center justify-center gap-2 rounded-xl bg-gradient-to-r from-indigo-600 to-sky-600 px-4 py-3 font-bold text-white transition-all hover:scale-[1.01] hover:shadow-lg hover:shadow-indigo-900/50 disabled:opacity-40 disabled:hover:scale-100"
          >
            学習をはじめる <ArrowRight size={18} />
          </button>
        </div>
      </div>
    );
  }

  // ログイン後の初期ロード中はスピナー（真っ白防止）
  if (loading && screen === "dashboard" && Object.keys(history).length === 0 && !showResumeModal) {
    return <Loader label="学習データを同期しています..." />;
  }

  /* ===================== 画面：ダッシュボード ===================== */
  if (screen === "dashboard") {
    return (
      <div className="min-h-screen bg-slate-950 text-slate-100 font-sans">
        <Header userId={userId} />
        <div className="mx-auto max-w-4xl px-4 pb-16 pt-6 space-y-6">
          {/* 統計 + チャート */}
          <div className="grid grid-cols-1 gap-4 md:grid-cols-2">
            <div className="rounded-2xl border border-slate-800 bg-slate-900/60 p-5">
              <div className="mb-3 flex items-center gap-2 text-slate-300">
                <BarChart2 size={18} className="text-sky-400" />
                <span className="font-bold">学習サマリー</span>
              </div>
              <div className="grid grid-cols-3 gap-2 text-center">
                <Stat label="正解" value={stats.correct} color="text-green-400" />
                <Stat label="不正解" value={stats.wrong} color="text-red-400" />
                <Stat label="未着手" value={stats.untouched} color="text-slate-400" />
              </div>
              <div className="mt-3 text-center text-xs text-slate-500">
                要復習 {stats.reviewCount} 問 / 全 {stats.total} 問
              </div>
            </div>
            <div className="rounded-2xl border border-slate-800 bg-slate-900/60 p-5">
              <ResponsiveContainer width="100%" height={150}>
                <BarChart data={chartData}>
                  <XAxis dataKey="name" stroke="#94a3b8" fontSize={12} />
                  <YAxis allowDecimals={false} stroke="#94a3b8" fontSize={12} />
                  <Tooltip
                    contentStyle={{
                      background: "#0f172a",
                      border: "1px solid #334155",
                      borderRadius: "8px",
                      color: "#e2e8f0",
                    }}
                  />
                  <Bar dataKey="value" radius={[6, 6, 0, 0]}>
                    {chartData.map((d, i) => (
                      <Cell key={i} fill={d.fill} />
                    ))}
                  </Bar>
                </BarChart>
              </ResponsiveContainer>
            </div>
          </div>

          {/* モード選択 */}
          <div>
            <h2 className="mb-3 text-sm font-bold text-slate-400">出題モードを選択</h2>
            <div className="grid grid-cols-1 gap-3 sm:grid-cols-3">
              <ModeButton
                title="すべての問題"
                desc={`全 ${stats.total} 問`}
                onClick={() => startMode("all")}
              />
              <ModeButton
                title="前回不正解のみ"
                desc={`${stats.wrong} 問`}
                onClick={() => startMode("wrong")}
                disabled={stats.wrong === 0}
              />
              <ModeButton
                title="要復習のみ"
                desc={`${stats.reviewCount} 問`}
                onClick={() => startMode("review")}
                disabled={stats.reviewCount === 0}
              />
            </div>
          </div>

          {/* 履歴グリッド */}
          <div>
            <h2 className="mb-3 text-sm font-bold text-slate-400">解答状況一覧（タップで開始）</h2>
            <div className="grid grid-cols-4 gap-2 sm:grid-cols-6">
              {QUESTIONS.map((q, i) => {
                const h = history?.[q.id];
                const status = !h ? "untouched" : h.correct ? "correct" : "wrong";
                const cls =
                  status === "correct"
                    ? "border-green-500/50 bg-green-500/10 text-green-300"
                    : status === "wrong"
                    ? "border-red-500/50 bg-red-500/10 text-red-300"
                    : "border-slate-700 bg-slate-900/60 text-slate-400";
                return (
                  <button
                    key={q.id}
                    onClick={() => startMode("all", i)}
                    className={`relative rounded-xl border p-2 text-center transition-all hover:scale-[1.03] ${cls}`}
                  >
                    {reviews?.[q.id] && (
                      <span className="absolute right-1 top-1 h-2 w-2 rounded-full bg-sky-400" />
                    )}
                    <div className="text-base font-bold">{q.id}</div>
                    <div className="mt-0.5 text-[10px] leading-tight opacity-80">
                      {status === "correct" ? "正解" : status === "wrong" ? "不正解" : "未着手"}
                    </div>
                    {h?.ts && (
                      <div className="mt-0.5 text-[8px] text-slate-500">
                        {new Date(h.ts).toLocaleDateString("ja-JP", {
                          month: "numeric",
                          day: "numeric",
                        })}
                      </div>
                    )}
                  </button>
                );
              })}
            </div>
          </div>
        </div>

        {/* 途中再開モーダル */}
        {showResumeModal && pendingProgress && (
          <ResumeModal
            pending={pendingProgress}
            onResume={handleResume}
            onRestart={handleRestart}
          />
        )}
      </div>
    );
  }

  /* ===================== 画面：クイズ ===================== */
  if (screen === "quiz") {
    const q = quizList[currentIndex];
    if (!q) {
      return <Loader label="問題を読み込んでいます..." />;
    }
    const isCorrect = selected === q.answer;
    return (
      <div className="min-h-screen bg-slate-950 text-slate-100 font-sans">
        <Header userId={userId} onHome={goHome} />
        <div className="mx-auto max-w-3xl px-4 pb-20 pt-5">
          {/* 進捗バー */}
          <div className="mb-4 flex items-center justify-between text-xs text-slate-400">
            <span>
              {currentIndex + 1} / {quizList.length} 問
            </span>
            <span className="rounded-full bg-slate-800 px-2 py-0.5">
              モード：{mode === "all" ? "すべて" : mode === "wrong" ? "前回不正解" : "要復習"}
            </span>
          </div>
          <div className="mb-5 h-1.5 w-full overflow-hidden rounded-full bg-slate-800">
            <div
              className="h-full rounded-full bg-gradient-to-r from-indigo-500 to-sky-500 transition-all"
              style={{ width: `${((currentIndex + 1) / quizList.length) * 100}%` }}
            />
          </div>

          {/* 問題カード */}
          <div className="rounded-2xl border border-slate-800 bg-slate-900/60 p-5 shadow-xl">
            <div className="mb-3 flex flex-wrap items-center gap-2">
              <span className="rounded-lg bg-gradient-to-r from-indigo-600 to-sky-600 px-2.5 py-1 text-xs font-bold text-white">
                問題 {q.id}
              </span>
              <span className="rounded-lg border border-indigo-500/40 bg-indigo-500/10 px-2.5 py-1 text-xs font-semibold text-indigo-300">
                過去問　{q.source}
              </span>
              {q.title && <span className="text-xs text-slate-400">{q.title}</span>}
            </div>

            <p className="whitespace-pre-wrap leading-relaxed text-slate-100 text-[15px]">
              {q.question}
            </p>

            {/* 出題用図表（解答情報を含まないニュートラルな与条件図） */}
            {q.figures?.map((fk) => (
              <div key={fk}>
                {FIG_TITLES[fk] ? (
                  <div className="mt-3 text-sm font-bold text-slate-300">{FIG_TITLES[fk]}</div>
                ) : null}
                <Figure keyName={fk} />
              </div>
            ))}

            {/* 選択肢 */}
            <div className="mt-5 space-y-2.5">
              {q.choices.map((c) => {
                let cls =
                  "border-slate-700 bg-slate-950/60 hover:border-indigo-500 hover:bg-slate-800/60";
                if (isAnswered) {
                  if (c.label === q.answer)
                    cls = "border-green-500 bg-green-500/15";
                  else if (c.label === selected)
                    cls = "border-red-500 bg-red-500/15";
                  else cls = "border-slate-800 bg-slate-950/40 opacity-60";
                }
                return (
                  <button
                    key={c.label}
                    onClick={() => handleAnswer(c.label)}
                    disabled={isAnswered}
                    className={`flex w-full items-start gap-3 rounded-xl border p-3.5 text-left transition-all ${cls}`}
                  >
                    <span
                      className={`mt-0.5 flex h-6 w-6 flex-none items-center justify-center rounded-full text-xs font-bold ${
                        isAnswered && c.label === q.answer
                          ? "bg-green-500 text-white"
                          : isAnswered && c.label === selected
                          ? "bg-red-500 text-white"
                          : "bg-slate-700 text-slate-200"
                      }`}
                    >
                      {isAnswered && c.label === q.answer ? (
                        <Check size={14} />
                      ) : isAnswered && c.label === selected ? (
                        <X size={14} />
                      ) : (
                        c.label
                      )}
                    </span>
                    <span className="text-[15px] leading-relaxed text-slate-100">{c.text}</span>
                  </button>
                );
              })}
            </div>
          </div>

          {/* 解説 */}
          {isAnswered && (
            <div className="mt-5 rounded-2xl border border-slate-800 bg-slate-900/60 p-5 shadow-xl">
              <div
                className={`mb-4 flex items-center gap-2 text-lg font-bold ${
                  isCorrect ? "text-green-400" : "text-red-400"
                }`}
              >
                {isCorrect ? <Check size={22} /> : <X size={22} />}
                {isCorrect ? "正解！" : "不正解"}
                <span className="ml-1 text-sm font-normal text-slate-400">
                  （正解：{q.answer}）
                </span>
              </div>

              <ExplanationSegments segments={q.explanation} />

              {/* 要復習 */}
              <label className="mt-5 flex cursor-pointer items-center gap-3 rounded-xl border border-slate-700 bg-slate-950/60 p-3">
                <input
                  type="checkbox"
                  checked={!!reviews?.[q.id]}
                  onChange={() => toggleReview(q.id)}
                  className="h-5 w-5 accent-sky-500"
                />
                <span className="flex items-center gap-2 text-sm text-slate-200">
                  <HelpCircle size={16} className="text-sky-400" />
                  この問題を要復習リストに登録する
                </span>
              </label>

              <div className="mt-5 flex gap-3">
                <button
                  onClick={goHome}
                  className="flex items-center justify-center gap-2 rounded-xl border border-slate-700 bg-slate-800 px-4 py-3 text-sm font-semibold text-slate-200 transition-all hover:bg-slate-700"
                >
                  <Home size={16} /> ホーム
                </button>
                <button
                  onClick={nextQuestion}
                  className="flex flex-1 items-center justify-center gap-2 rounded-xl bg-gradient-to-r from-indigo-600 to-sky-600 px-4 py-3 font-bold text-white transition-all hover:scale-[1.01] hover:shadow-lg hover:shadow-indigo-900/50"
                >
                  {currentIndex + 1 >= quizList.length ? "結果を見る" : "次の問題へ"}
                  <ChevronRight size={18} />
                </button>
              </div>
            </div>
          )}
        </div>
      </div>
    );
  }

  /* ===================== 画面：結果 ===================== */
  if (screen === "result") {
    return (
      <div className="min-h-screen bg-slate-950 text-slate-100 font-sans">
        <Header userId={userId} />
        <div className="mx-auto flex max-w-md flex-col items-center px-4 pt-16 text-center">
          <div className="mb-6 flex h-20 w-20 items-center justify-center rounded-3xl bg-gradient-to-br from-indigo-500 to-sky-500 shadow-xl shadow-indigo-900/50">
            <Check size={40} className="text-white" />
          </div>
          <h2 className="text-2xl font-bold">全問完走しました！</h2>
          <p className="mt-2 text-slate-400">お疲れさまでした。進捗はリセットされました。</p>
          <div className="mt-6 grid w-full grid-cols-3 gap-2">
            <Stat label="正解" value={stats.correct} color="text-green-400" />
            <Stat label="不正解" value={stats.wrong} color="text-red-400" />
            <Stat label="要復習" value={stats.reviewCount} color="text-sky-400" />
          </div>
          <button
            onClick={() => {
              isFirstLoad.current = false;
              setScreen("dashboard");
            }}
            className="mt-8 flex w-full items-center justify-center gap-2 rounded-xl bg-gradient-to-r from-indigo-600 to-sky-600 px-4 py-3 font-bold text-white transition-all hover:scale-[1.01]"
          >
            <Home size={18} /> ダッシュボードへ戻る
          </button>
        </div>
      </div>
    );
  }

  return <Loader label="Loading..." />;
}

/* ===================== 小コンポーネント ===================== */
function Header({ userId, onHome }) {
  return (
    <header className="sticky top-0 z-10 border-b border-slate-800 bg-slate-950/80 backdrop-blur">
      <div className="mx-auto flex max-w-4xl items-center justify-between px-4 py-3">
        <button
          onClick={onHome}
          disabled={!onHome}
          className="flex items-center gap-2 text-left disabled:cursor-default"
        >
          <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-gradient-to-br from-indigo-500 to-sky-500">
            <BookOpen size={16} className="text-white" />
          </div>
          <div>
            <div className="text-sm font-bold leading-tight">過去問セレクト演習 4-6</div>
            <div className="text-[10px] text-slate-500">情報システムの開発</div>
          </div>
        </button>
        <div className="flex items-center gap-1.5 rounded-full border border-slate-800 bg-slate-900 px-3 py-1 text-xs text-slate-300">
          <User size={13} className="text-indigo-400" />
          {userId}
        </div>
      </div>
    </header>
  );
}

function Stat({ label, value, color }) {
  return (
    <div className="rounded-xl border border-slate-800 bg-slate-950/60 py-3">
      <div className={`text-2xl font-bold ${color}`}>{value}</div>
      <div className="text-[11px] text-slate-500">{label}</div>
    </div>
  );
}

function ModeButton({ title, desc, onClick, disabled }) {
  return (
    <button
      onClick={onClick}
      disabled={disabled}
      className="group flex items-center justify-between rounded-2xl border border-slate-800 bg-slate-900/60 p-4 text-left transition-all hover:scale-[1.01] hover:border-indigo-500/60 hover:shadow-lg hover:shadow-indigo-950/40 disabled:opacity-40 disabled:hover:scale-100"
    >
      <div>
        <div className="font-bold text-slate-100">{title}</div>
        <div className="text-xs text-slate-500">{desc}</div>
      </div>
      <ChevronRight size={20} className="text-slate-600 group-hover:text-indigo-400" />
    </button>
  );
}

function ResumeModal({ pending, onResume, onRestart }) {
  const modeLabel =
    pending.progressMode === "all"
      ? "すべての問題"
      : pending.progressMode === "wrong"
      ? "前回不正解のみ"
      : "要復習のみ";
  return (
    <div className="fixed inset-0 z-50 flex items-center justify-center bg-slate-950/80 p-4 backdrop-blur-sm">
      <div className="w-full max-w-sm rounded-2xl border border-indigo-500/40 bg-slate-900 p-6 shadow-2xl shadow-indigo-950/60">
        <div className="mb-3 flex items-center gap-2 text-indigo-300">
          <RefreshCw size={18} />
          <span className="font-bold">途中再開</span>
        </div>
        <p className="text-sm leading-relaxed text-slate-200">
          前回は<span className="font-bold text-sky-300">【問題 {pending.progressIndex}】</span>
          まで進んでいます。中断したモード（
          <span className="font-bold text-indigo-300">{modeLabel}</span>
          ）の続きから再開しますか？
        </p>
        <div className="mt-6 flex flex-col gap-2">
          <button
            onClick={onResume}
            className="flex items-center justify-center gap-2 rounded-xl bg-gradient-to-r from-indigo-600 to-sky-600 px-4 py-3 font-bold text-white transition-all hover:scale-[1.01]"
          >
            続きから再開する <ArrowRight size={16} />
          </button>
          <button
            onClick={onRestart}
            className="rounded-xl border border-slate-700 bg-slate-800 px-4 py-3 text-sm font-semibold text-slate-200 transition-all hover:bg-slate-700"
          >
            最初から始める
          </button>
        </div>
      </div>
    </div>
  );
}